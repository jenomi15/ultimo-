public class ResultadoDados {
    private int soma;
    private boolean iguais;

    public ResultadoDados(int soma, boolean iguais) {
        this.soma = soma;
        this.iguais = iguais;
    }

    public int getSoma() {
        return soma;
    }

    public boolean isIguais() {
        return iguais;
    }
}
